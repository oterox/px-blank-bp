<?php
function plu_admin_enqueue() {

    wp_enqueue_script('plupload-all');
 
    wp_register_script('myplupload', get_template_directory_uri() . '/core/myplupload.js', array('jquery'));
    wp_enqueue_script('myplupload');
 
    wp_register_style('myplupload', get_template_directory_uri() . '/core/myplupload.css');
    wp_enqueue_style('myplupload');
}
add_action( 'admin_enqueue_scripts', 'plu_admin_enqueue' );
?>


<?php
